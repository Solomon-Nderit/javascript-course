// In JS, the logical operators &&, ||, and ! can be used with non-boolean values.
// The non-boolean values that are considered falsy in JavaScript include:
// 0, "", null, undefined, NaN, and false.

// Sample
let value1 = 0;
let value2 = "Hello";

// Logical AND (&&) with non-boolean values
let andResult = value1 && value2;
console.log('Logical AND result:', andResult);