// Arithmetic operators
let a = 10;
let b = 3;

console.log('a - b =', a - b); // Subtraction
console.log('a + b =', a + b); // Addition
console.log('a * b =', a * b); // Multiplication
console.log('a / b =', a / b); // Division
console.log('a % b =', a % b); // Modulus

// Increment and compound assignment operators
let c = 5;
console.log('c before increment:', c);
c++; // Increment
console.log('c after c++:', c);

c += 2; // Add and assign
console.log('c after c += 2:', c);

c -= 1; // Subtract and assign
console.log('c after c -= 1:', c);

c *= 3; // Multiply and assign
console.log('c after c *= 3:', c);

c /= 2; // Divide and assign
console.log('c after c /= 2:', c);

c %= 4; // Modulus and assign
console.log('c after c %= 4:', c);